function joule_to_ev(joule)
    return joule * 6.242e+18
end

function ev_to_joule(ev)
    return ev / 6.242e+18
end

function barn_to_m2(barn)
    return barn * 1e-28
end

function m2_to_barn(m2)
    return m2 / 1e-28
end

function amu_to_kg(amu)
    return amu * 1.66054e-27
end

function kg_to_amu(kg)
    return kg / 1.66054e-27
end